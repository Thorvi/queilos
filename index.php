            <h2>Etiologia e Fatores de Risco</h2>
            <div class="causes-content">
                <p>A origem da fissura labiopalatina é multifatorial, envolvendo uma complexa interação entre fatores
                    genéticos e ambientais. Estudos como os de CEFAC e Barbosa da Silva (2005) apontam que:</p>

                <ul class="detailed-list">
                    <li><strong>Fatores genéticos:</strong> Cerca de 20-30% dos casos estão associados a histórico
                        familiar, com padrão de herança poligênica. Algumas síndromes genéticas como a síndrome de Van
                        der Woude também incluem a FLP em seu espectro.</li>
                    <li><strong>Fatores ambientais pré-natais:</strong> Incluem uso de álcool, tabaco e certos
                        medicamentos (como anticonvulsivantes e corticoides) durante o primeiro trimestre de gestação,
                        período crítico para a formação facial.</li>
                    <li><strong>Deficiências nutricionais:</strong> Principalmente de ácido fólico e outras vitaminas do
                        complexo B, que desempenham papel crucial no desenvolvimento embrionário.</li>
                    <li><strong>Idade materna avançada e diabetes gestacional:</strong> Também foram associados a maior
                        risco de ocorrência de FLP em alguns estudos epidemiológicos.</li>
                </ul>

                <p>É importante destacar que na maioria dos casos não é possível identificar uma causa única, sendo o
                    resultado da combinação de múltiplos fatores. Apesar dos avanços na pesquisa genética, ainda não há
                    consenso na literatura sobre todos os mecanismos envolvidos, reforçando a importância do
                    aconselhamento genético para famílias afetadas.</p>
